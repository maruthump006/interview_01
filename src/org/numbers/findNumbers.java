package org.numbers;

public class findNumbers {

	public static void main(String[] args) {
		System.out.println("EVEN NUMBERS");
		for (int i = 0; i <= 25; i+=2) {
			System.out.println(" "+i);
			
			}
		System.out.println("ODD NUMBERS");
		for (int i = 1; i <= 25; i+=2) {
			System.out.println(" "+i);
			
			}
	}
}

